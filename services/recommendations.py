    if ee_level in ['—Å—Ä–µ–¥–Ω–∏–π', '–≤—ã—Å–æ–∫–∏–π']:
        recommendations.append("üí° **–î–ª—è —ç–º–æ—Ü–∏–æ–Ω–∞–ª—å–Ω–æ–≥–æ –∏—Å—Ç–æ—â–µ–Ω–∏—è (EE):**")
        if ee_level == '—Å—Ä–µ–¥–Ω–∏–π':
            recommendations.append("‚Ä¢ –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ —á–µ—Ç–∫–æ–µ —Ä–∞–±–æ—á–µ–µ –≤—Ä–µ–º—è")
