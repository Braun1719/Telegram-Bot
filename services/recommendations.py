    if dp_level in ['—Å—Ä–µ–¥–Ω–∏–π', '–≤—ã—Å–æ–∫–∏–π']:
        recommendations.append("\nüí° **–î–ª—è –¥–µ–ø–µ—Ä—Å–æ–Ω–∞–ª–∏–∑–∞—Ü–∏–∏ (DP):**")
