    dp_level = interpretation.get('DP', {}).get('level', '–Ω–∏–∑–∫–∏–π')
    pa_level = interpretation.get('PA', {}).get('level', '–Ω–∏–∑–∫–∏–π')
    
    recommendations = []
    
